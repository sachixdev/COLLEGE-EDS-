#write a program to print numbers from 1 to n using for loop.
num=int(input("Enter a number :"))   #taking input from the user
for i in range(1,num+1):   #by for loop 
    print(i)
    i+=1


#write a program to print numbers from 1 to n using while loop.
num=int(input("Enter a number : "))
i=1   #initilization
while i<=num:    #condition
    print(i)   #prints number
    i+=1   #increment

