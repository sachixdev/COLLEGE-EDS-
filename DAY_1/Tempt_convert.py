# This program converts a temperature from Celsius to Fahrenheit.
cel=int(input("Enter the Temperature in Celcius: "))
Farentheit=(cel*9/5)+32 #converting the temperature from Celsius to Fahrenheit using the formula F = (C * 9/5) + 32
print("The Temperature in Farenthiet is : ",Farentheit) #printing the converted temperature in Fahrenheit to the user