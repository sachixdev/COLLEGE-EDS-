# This program calculates the area of a triangle given the length of its base and its height.
lenght=int (input("Enter the length of the triangle: ")) #taking input from the user and converting it to an integer
height=int (input("Enter the height of the triangle: ")) #taking input from the user and converting it to an integer
area=0.5*lenght*height #calculating the area of the triangle by multiplying the length and height and then dividing by 2
print("The area of the triangle is: ", area)