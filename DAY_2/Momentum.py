#Calculate the momentum of an object given its mass and velocity.
mass=int(input("Enter the mass in kg : "))
velocity=int(input("Enter the velocity in m/s : "))   
momentum=mass*velocity   #calculating the momentum by multiplying the mass and velocity of the object
print("The momentum is :  ",momentum , "kgm/s")    #printing the calculated momentum to the user in kgm/s units
